# Perform some math using the R builtin operators
num1 <- 1.5
num2 <- 2
num3 <- 7

# addition
num1 + num2 # 3.5

# subtraction
num2 - num1 # 0.5

# multiplication
num1 * num2 # 3

# division
num1 / num2 # 0.75

# exponent
num1 ^ num2 # 2.25

# modulus
num3 %% num2 # 1

# integer division
num3 %/% num2 # 3
